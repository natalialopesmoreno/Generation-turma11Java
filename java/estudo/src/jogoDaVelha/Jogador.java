package jogoDaVelha;

public class Jogador 
{

	private String marcacao;
	private String nome;
	
	

	public String getMarcacao() 
	{
		return marcacao;
	}

	public void setMarcacao(String marcacao) 
	{
		this.marcacao = marcacao;
	}

	public String getNome() 
	{
		return nome;
	}

	public void setNome(String nome) 
	{
		this.nome = nome;
	}
	
}
